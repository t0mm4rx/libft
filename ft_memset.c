#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned char	*tab;
	unsigned int	i;

	tab = s;
	i = 0;
	while (i < n)
	{
		tab[i++] = c;
	}
	return (s);
}
